    public void appendToConsole(String message) {
        DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
        Calendar cal = Calendar.getInstance();
        consoleArea.append(dateFormat.format(cal.getTime()) + " - " + message + "\n");
    }
