    private void initUsernames() {
        for (UserVO user: userService.getUsers()) {
            defaultListModel.addElement(user.getUsername());
        }
    }
    
    private void startButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_startButtonMouseClicked
        connectionWorker = new ReceiveConnectionWorker(this);
        connectionWorker.execute();
        startButton.setEnabled(false);
        stopButton.setEnabled(true);
    }//GEN-LAST:event_startButtonMouseClicked

    private void stopButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_stopButtonMouseClicked
        connectionWorker.cancel(true);
        stopButton.setEnabled(false);
        startButton.setEnabled(true);
    }//GEN-LAST:event_stopButtonMouseClicked

    private void createButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_createButtonMouseClicked
        String userName = usernameField.getText();
        String password = String.valueOf(passwordField.getPassword());
        UserVO user = userService.createUser(userName, password);
        defaultListModel.addElement(user.getUsername());
    }//GEN-LAST:event_createButtonMouseClicked

    private void deleteButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteButtonMouseClicked
        String selectedUsername = usersList.getSelectedValue().toString();
        System.out.println(selectedUsername);
        userService.deleteUserByName(selectedUsername);
    }//GEN-LAST:event_deleteButtonMouseClicked

